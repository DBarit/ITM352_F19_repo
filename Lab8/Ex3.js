//Exercise 3a: for-loop
var age = 20;
for (num = 0; num < age; num++) {
    console.log(`Are you ${num} years olds?`);
}
console.log(`You must be ${num} years old!`);

//Exercise 3b: for-loop
var age = 20;
for (num = 50; num > age; num--) {
    console.log(`Are you ${num} years olds?`);
}
console.log(`You must be ${num} years old!`);
